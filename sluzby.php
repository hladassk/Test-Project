<h3>2. Odkvapové systémy</h3>

<p>
V súčasnosti je najpoužívanejším materiálom na výrobu klampiarskych prvkov plech. Používa sa predovšetkým pozinkovaný plech. 
Ďalšími klasickými klampiarskymi materiálmi sú oceľový, hliníkový, medený a zinkový plech. 

<br /><br />
Medzi novšie materiály patrí plast a hliník chránený vysoko odolným lakom, ktorý zvyšuje jeho úžitkové vlastnosti a predlžuje životnosť. Ďalším moderným materiálom je aluzinok – zliatina zinku a hliníka, ktorého životnosť je šesťkrát dlhšia ako životnosť pozinkovaného plechu.
<br /><br />
<strong>Najpoužívanejšie plechy:</strong><br /><br />
<em>Oceľový plech</em> v  súčasnosti sa používa predovšetkým na výrobu hákov. Je známy tým, že je potrebné ho pravidelne ošetrovať. Najlacnejším spôsobom je náter.
<br /><br />
<em>Pozinkovaný plech</em> je oceľový s nanesenou niekoľko mikrometrovou vrstvou zinku. Jeho výhodou sú relatívne nízke zaobstáravacie náklady, nevýhodou krátka životnosť a nevyhnutnosť pravidelného ošetrovania ochranným náterom.
<br /><br />
<em>Hliníkový plech</em> sa vyrába elektrolýzou z prírodnej látky zvanej bauxit. V praxi sa dá použiť rovnako ako pozinkovaný plech.  Jeho najväčšou výhodou je malá hmotnosť a hlavnými nevýhodami lámavosť a nemožnosť spájania spájkovaním. Jeho cena je iba o niečo vyššia ako cena pozinkovaného plechu, jeho životnosť je asi štyrikrát dlhšia.
<br /><br />
<em>Medený plech</em> sa používa vo forme niekoľkých druhov zliatin. Nevyžaduje povrchovú úpravu, sám si atmosférickými vplyvmi vytvára ochrannú vrstvu – patinu. Na použitie v klampiarstve má najlepšie vlastnosti. Je ľahký, tvarovateľný, mäkký, dá sa spájať spájkovaním a nevyžaduje nijakú údržbu. Jeho životnosť je takmer neobmedzená, závisí iba na vonkajších negatívnych vplyvoch (kyslé dažde, UV žiarenie a pod.). Jeho najväčšou nevýhodou je vysoká zaobstarávacia cena. 
<br /><br />
<em>Zinkový plech</em> sa v stavebníctve používa predovšetkým vo forme zušľachteného zinku pod názvom titánzinok. Má podobné vlastnosti ako medený plech. Nevyžaduje nijaké ďalšie protikorózne úpravy. Jeho životnosť je však časovo obmedzená, pretože sa vplyvom prostredia stenčuje. Jeho cena sa pohybuje na úrovni medeného plechu.
<br />
Na našom trhu sa začínajú presadzovať aj v zahraničí už dlhší čas používané materiály. 
Predovšetkým pozinkovaný plech s ochrannou vrstvou z plastu. Tento plech je hĺbkovo žiarovo pozinkovaný a po pastivácii je upravený základným náterom a vrstvou plastu. Materiál výborne kombinuje vlastnosti ocele a plastu. Je pevný, húževnatý, tepelne stabilný, plastová vrstva zabraňuje korózii a zabezpečuje farebnú stálosť. Jeho najväčšou výhodou je dlhá životnosť a relatívne nízka cena. Nedá sa spájkovať, iba falcovať.
<br /><br />
<em>Aluzinkový plech</em> je oceľový plech, na ktorom je nanesená zliatina hliníka, zinku a kremíka. Jeho životnosť záleží na vonkajšom prostredí, ale oproti pozinkovanému plechu je šesťkrát dlhšia. 
<br /><br />
Plast – ide o formu tvrdeného plastu, z ktorého sa vyrába najmä odkvapový systém.

<br /><br />
<strong>Kombinácie materiálov</strong><br />
Pri výbere kovovej strešnej krytiny a klampiarskych prvkov musíme dodržiavať určité zásady. Obe časti strechy by mali byť z rovnakého materiálu alebo z materiálu s rovnakým elektrolytickým potenciálom, aby v prípade vlhkosti nedochádzalo k elektrolytickej korózii. Napríklad na železo, hliník a zinok má negatívny vplyv meď. 
<br /><br />
Rovnako je problematické položiť na strechu krytinu, ktorá má životnosť dlhšiu niekoľko desiatok rokov  klampiarske prvky z pozinkovaného plechu, ktorého životnosť sa bez pravidelného ošetrovania odhaduje na päť až desať rokov. Je ťažko predstaviteľné  ako každé dva roky vstupuje majiteľ na svoju strechu z kvalitnej krytiny a natiera klampiarske prvky! 


</p> 



